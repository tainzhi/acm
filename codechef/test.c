#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#include <string.h>

int main()
{
    int n;
    while (scanf("%d", &n) != EOF)
    {
        while (n < 0)
            printf("%d\n", n = (n+11) % 11);
    }
    return 0;
}
